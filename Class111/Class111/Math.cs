using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Class111
{
    class Math
    {
        public static int MultiplybyTwo(int numberOne)
        {
            int product = numberOne * 2;

            return product;

        }
        public static int DividebyTwo(int numberOne)
        {

            int quotient = numberOne / 2;

            return quotient;
        }
        public static int AddTwo(int numberOne)
        {

            int sum = numberOne + 2;

            return sum;
        }

    }
}
